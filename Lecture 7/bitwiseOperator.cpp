#include<iostream>
using namespace std;

int main()
{
    int a = 4, b = 8;
    cout<<(a & b)<<'\n';   
    cout<<(a | b)<<'\n';
    cout<<(a ^ b)<<'\n';
    cout<<(3 ^ 7)<<'\n';
    cout<<(4 << 1)<<'\n';
    cout<<(10 << 2)<<'\n';
    cout<<(10 >> 1)<<'\n';
    cout<<(8 >> 2)<<'\n';
    return 0;
}